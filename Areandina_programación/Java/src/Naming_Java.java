public class Naming_Java {
    public static void main(String[] args) {
        // Cada variable debe tener un nombre único
        String nombre = "Lionel";
        String apellido = "Messi";
        int edad_Messi= 34;
        int numero_Messi = 10;
        String nacionalidad = "Argentino";
        String equipo = "Inter Miami";
        boolean esTitular = true;
        int valor_Messi = 20000000;

        // Imprimir variables
        System.out.println("Nombre: " + nombre);
        System.out.println("Apellido: " + apellido);
        System.out.println("Edad: " + edad_Messi);
        System.out.println("Número: " + numero_Messi);
        System.out.println("Nacionalidad: " + nacionalidad);
        System.out.println("Equipo: " + equipo);
        System.out.println("Es titular: " + esTitular);
        System.out.println("Valor: " + valor_Messi);
    }
}
