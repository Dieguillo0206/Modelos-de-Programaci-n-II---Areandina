public class Cast {
    public static void main(String[] args) {
        // Si Messi lleva 100 goles en 1 año, ¿cuántos goles lleva por mes?
        double golesAnuales = 100;
        double meses = 12;
        double golesMensuales = golesAnuales / meses;
        System.out.println("Messi lleva " + golesMensuales + " goles por mes.");
    }
}
