public class DataTypes {
    public static void main(String[] args) {
        // byte
        byte nB = 123;

        // enteros (int)
        int edad = 23;

       // float
        float nF = 123.456f;

        // decimales (double)
        double altura = 1.82;

        // short (rango con numeros peque√±os)
        short nS = 123;

        // long (rango con numeros grandes)
        long nL = 123456789;


        System.out.println("Numero byte: " + nB);
        System.out.println("Edad: " + edad);
        System.out.println("Numero float: " + nF);
        System.out.println("Altura: " + altura);
        System.out.println("Numero short: " + nS);
        System.out.println("Numero long: " + nL);
    }
}
